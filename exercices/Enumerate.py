# Use a loop to go through fruit list
# For each element, display the index and the element (starting from 1)

fruits = ["apple", "banana", "cherry", "pear", "grape", "orange"]
for i, v in enumerate(fruits, 1):
    print(f'For index {i} fruit is: {v} ')

